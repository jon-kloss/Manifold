//! Renders the Rust token constants to renderer/src/tokens/{tokens.css,tokens.ts}.
//! Run from repo root: `cargo run -p app --no-default-features --bin gen-tokens`.
//! CI verifies the output is committed unchanged.

use app::tokens;

const HEADER: &str = "GENERATED by gen-tokens from crates/app/src/tokens.rs — do not edit by hand.";

fn main() -> std::io::Result<()> {
    let mut css = format!("/* {HEADER} */\n:root {{\n");
    let mut ts = format!("// {HEADER}\nexport const tokens = {{\n");
    for (group, t) in tokens::all() {
        css.push_str(&format!("  --{}: {};\n", t.name, t.value));
        let key = t.name.replace('-', "_");
        ts.push_str(&format!("  /** {group} */ {key}: {:?},\n", t.value));
    }
    css.push_str("}\n");
    ts.push_str("} as const;\n\nexport type TokenName = keyof typeof tokens;\n");

    let dir = std::path::Path::new("renderer/src/tokens");
    std::fs::create_dir_all(dir)?;
    std::fs::write(dir.join("tokens.css"), css)?;
    std::fs::write(dir.join("tokens.ts"), ts)?;
    eprintln!("tokens written to {}", dir.display());
    Ok(())
}
